let _uiLocale = "zh-TW";

function setUILocale(loc) {
  _uiLocale = loc === UserPreferenceLocale.ZH_CN ? "zh-CN" : "zh-TW";
  document.documentElement.lang = _uiLocale === "zh-CN" ? "zh-CN" : "zh-TW";
}

function getUILocale() {
  return _uiLocale;
}

function t(key) {
  const table = _uiLocale === "zh-CN" ? I18N_ZH_CN : I18N_ZH_TW;
  const s = table[key];
  if (typeof s === "string") return s;
  const fb = I18N_ZH_TW[key];
  return typeof fb === "string" ? fb : key;
}

function refreshWelcomeStaticI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const k = el.getAttribute("data-i18n");
    if (k) el.textContent = t(k);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const k = el.getAttribute("data-i18n-aria");
    if (k) el.setAttribute("aria-label", t(k));
  });
  const closeBtn = document.getElementById("welcome-close");
  if (closeBtn) closeBtn.setAttribute("aria-label", t("welcome.closeAria"));
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const k = el.getAttribute("data-i18n-title");
    if (k) el.setAttribute("title", t(k));
  });
}

function refreshPaletteChromeI18n() {
  const header = document.getElementById("palette-header");
  const span = header && header.querySelector("span");
  if (span) span.textContent = t("palette.title");
  const btn = document.getElementById("btn-add-palette");
  if (btn) {
    btn.textContent = t("palette.addCurrentBrush");
    btn.title = t("palette.addCurrentBrushTitle");
  }
  const ime = document.getElementById("type-ime-hook");
  if (ime) ime.setAttribute("aria-label", t("ime.ariaLabel"));
}

function refreshDocumentTitle() {
  if (typeof APP_RELEASE === "undefined") return;
  document.title = t("app.titleWithVersion").replace(/\{v\}/g, APP_RELEASE.version);
}
